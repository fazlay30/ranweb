    <dt><strong>Principles of RoR:</strong></dt>
    <dd>Ruby on Rails is known as an MVC(model-view-controller) full stack framework. The code is separated into 3 interconnected layers:
    * Model contains the logic of an application, all the essential data and high level classes.
    * View is the UI representation of the data present in Model. It’s what users interact with and see on their screens.
    * Controller connects Model and View, receives user inputs, and decides how to handle the input.</dd>

