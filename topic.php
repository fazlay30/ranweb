    <dt><strong>Defination of Technology (Ruby on rails)</strong></dt>
    <dd>Ruby on rails is a web development framework that gives Rails developers a time saving method for writing code. It is defined as a package library called RubyGem, installed using the command line interface of the operating system. It is a collection of code libraries, which offer a readymate solution for repetitive tasks like developing tables, forms or menus on the website. It is categorized as back-end or server-side web application development platform. The compactness and elegance of the language is the main attraction of it.
