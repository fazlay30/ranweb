    <h2>How Ruby on Rails was developed and by whom:</h2>
    <p>David Heinemeir Hansson was working on the code base for basecamp back in 2003. Due to complexity of basecamp, he ended up creating a new custom framework in order to build it.
    He used Ruby to create the framework and referred to it as Ruby on Rails. He released that in 2004, but did not share commit rights to the project until February 2005.
    He was fascinated with object-oriented languages. But none that existed at the time appealed to him. Here’s his quote from a past he made back in 1999:<br>
    <br>
    “As a language maniac and OO fan for 15 years, I really wanted a genuine object-oriented, easy to use scripting language. I looked for but couldn’t find one. So I decided to make it.”</p>
